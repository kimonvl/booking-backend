-- ---------- PERSONAL INFO ----------
CREATE TABLE IF NOT EXISTS personal_info (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country_code VARCHAR(2) NOT NULL,
    first_name   VARCHAR(120) NOT NULL,
    last_name    VARCHAR(120) NOT NULL,
    CONSTRAINT fk_personal_info_country
        FOREIGN KEY (country_code) REFERENCES countries(code)
);

-- ---------- USERS ----------
CREATE TABLE IF NOT EXISTS users (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid             UUID NOT NULL,
    email            VARCHAR(320) NOT NULL UNIQUE,
    password_hash    TEXT NOT NULL,
    role_id          BIGINT NOT NULL,
    personal_info_id BIGINT,
    enabled          BOOLEAN NOT NULL DEFAULT TRUE,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted          BOOLEAN NOT NULL DEFAULT FALSE,
    deleted_at       TIMESTAMPTZ,
    CONSTRAINT fk_users_role
        FOREIGN KEY (role_id) REFERENCES roles(id),
    CONSTRAINT fk_users_personal_info
        FOREIGN KEY (personal_info_id) REFERENCES personal_info(id)
        ON DELETE SET NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_users_uuid ON users(uuid);
CREATE UNIQUE INDEX IF NOT EXISTS idx_users_email ON users(email);
CREATE UNIQUE INDEX IF NOT EXISTS uq_users_personal_info_id
    ON users(personal_info_id)
    WHERE personal_info_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_users_role_id ON users(role_id);

-- ---------- REFRESH TOKENS ----------
CREATE TABLE IF NOT EXISTS refresh_tokens (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid       UUID NOT NULL,
    user_id    BIGINT NOT NULL,
    token      VARCHAR(200) NOT NULL UNIQUE,
    expires_at TIMESTAMPTZ NOT NULL,
    revoked    BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted    BOOLEAN NOT NULL DEFAULT FALSE,
    deleted_at TIMESTAMPTZ,
    CONSTRAINT fk_refresh_tokens_user
        FOREIGN KEY (user_id) REFERENCES users(id)
        ON DELETE CASCADE
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_refresh_tokens_uuid ON refresh_tokens(uuid);
CREATE UNIQUE INDEX IF NOT EXISTS idx_refresh_token_token ON refresh_tokens(token);
CREATE INDEX IF NOT EXISTS idx_refresh_token_user_id ON refresh_tokens(user_id);
-- ---------- ADDRESSES ----------
CREATE TABLE IF NOT EXISTS addresses (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid          UUID NOT NULL,
    country_code  VARCHAR(2) NOT NULL,
    city          VARCHAR(120) NOT NULL,
    postcode      VARCHAR(32),
    street        VARCHAR(200) NOT NULL,
    street_number VARCHAR(32),
    floor_number  VARCHAR(10),
    lat           DOUBLE PRECISION,
    lng           DOUBLE PRECISION,
    CONSTRAINT fk_addresses_country
        FOREIGN KEY (country_code) REFERENCES countries(code)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_addresses_uuid ON addresses(uuid);
CREATE INDEX IF NOT EXISTS idx_addresses_city ON addresses(city);
CREATE INDEX IF NOT EXISTS idx_addresses_country ON addresses(country_code);
CREATE INDEX IF NOT EXISTS idx_addresses_postcode ON addresses(postcode);

-- ---------- PROPERTIES ----------
