-- ---------- MESSAGES ----------
CREATE TABLE IF NOT EXISTS messages (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid       UUID NOT NULL,
    chat_id    BIGINT NOT NULL,
    author_id  BIGINT NOT NULL,
    content    VARCHAR(4000),
    photo      VARCHAR(600),
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted    BOOLEAN NOT NULL DEFAULT FALSE,
    deleted_at TIMESTAMPTZ,
    CONSTRAINT fk_messages_chat
        FOREIGN KEY (chat_id) REFERENCES chats(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_messages_author
        FOREIGN KEY (author_id) REFERENCES users(id)
        ON DELETE CASCADE,
    CONSTRAINT chk_messages_content_or_photo CHECK (
        (content IS NOT NULL AND length(btrim(content)) > 0)
        OR
        (photo IS NOT NULL AND length(btrim(photo)) > 0)
    )
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_messages_uuid ON messages(uuid);
CREATE INDEX IF NOT EXISTS idx_messages_chat_created ON messages(chat_id, created_at);
